    public void getGR(){
        try {
            ResultSet rs = DatabaseConnection.getData("Select * from main where std_gr='"+std_gr.getText()+"'");
            if (rs.next()) {                
              recipient.setText(rs.getString("std_email"));
            }else if(!rs.next()){
                StartGUI.displayMessage("No record found!");
            }
        } catch (Exception ex) {
            Logger.getLogger(SendMail.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
