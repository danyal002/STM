    private void jPanel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MouseClicked
        Properties props = new Properties();
        String host = "smtp.gmail.com";
        String port = "465";
        props.put("mail.smtp.host", host);
        props.put("mail.smtp.auth", "true");
        props.put("mail.smtp.port", port);
        props.put("mail.smtp.socketFactory.port", port);
        props.put("mail.smtp.socketFactory.class", "javax.net.ssl.SSLSocketFactory");
        props.put("mail.smtp.socketFactory.fallback", "false");

        Session session = Session.getDefaultInstance(props,
                new javax.mail.Authenticator() {
            protected PasswordAuthentication getPasswordAuthentication() {
                return new PasswordAuthentication("danyal.ak002@gmail.com", "dantheman!$");
            }
        }
        );

        try {
            Message message = new MimeMessage(session);
            message.setFrom(new InternetAddress("geniusdan05@gmail.com"));
            message.setRecipients(Message.RecipientType.TO, InternetAddress.parse(recipient.getText()));
            message.setSubject(subject.getText());
            message.setText(body.getText());
            Transport.send(message);
            StartGUI.displayMessage("Mail Sent Successfully.");
            recipient.setText("");
            body.setText("");
            subject.setText("");
            jLabel1.setIcon(null);

        } catch (Exception e) {
            Logger.getLogger(SendMail.class.getName()).log(Level.SEVERE, null, e);
            StartGUI.displayMessage("Mail could not be sent.");
        }
    }//GEN-LAST:event_jPanel1MouseClicked

    private void recipientKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_recipientKeyReleased
       String get = recipient.getText();
       if(!get.matches("^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,6}$")){
           jLabel1.setIcon(new ImageIcon(getClass().getResource("/Images/Delete.png")));
       }else if(get.matches("^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,6}$")){
           jLabel1.setIcon(new ImageIcon(getClass().getResource("/Images/Check.png")));
       }
        
    }//GEN-LAST:event_recipientKeyReleased

